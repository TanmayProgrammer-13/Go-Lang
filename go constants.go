// Types Constants
package main
import "fmt"

const Coder int = 20 

func main() {
  fmt.Println(Coder)
}
// Untyped Constants
package main
import "fmt"

const Coder = 20 

func main() {
  fmt.Println(Coder)
}
// Multiple Constants

package main
import ("fmt")

